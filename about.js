// Current language
let currentLang = "pl";

// DOM Elements
const langButtons = document.querySelectorAll(".lang-btn");

// Initialize the page
document.addEventListener("DOMContentLoaded", function () {
	initializeLanguage();
	initializeAnimations();

	// Set language from localStorage or default to 'pl'
	const savedLang = localStorage.getItem("preferred-language") || "pl";
	switchLanguage(savedLang);
});

// Language switching functionality
function initializeLanguage() {
	langButtons.forEach((btn) => {
		btn.addEventListener("click", () => {
			const lang = btn.getAttribute("data-lang");
			switchLanguage(lang);
		});
	});
}

function switchLanguage(lang) {
	currentLang = lang;

	// Update active language button
	langButtons.forEach((btn) => {
		btn.classList.remove("active");
		if (btn.getAttribute("data-lang") === lang) {
			btn.classList.add("active");
		}
	});

	// Update all translatable elements
	const translatableElements = document.querySelectorAll("[data-lang-key]");
	translatableElements.forEach((element) => {
		const key = element.getAttribute("data-lang-key");
		if (aboutTranslations[lang] && aboutTranslations[lang][key]) {
			element.textContent = aboutTranslations[lang][key];
		}
	});

	// Update HTML lang attribute
	document.documentElement.lang = lang;

	// Save language preference
	localStorage.setItem("preferred-language", lang);
}

// Initialize animations
function initializeAnimations() {
	const observerOptions = {
		threshold: 0.1,
		rootMargin: "0px 0px -50px 0px",
	};

	const observer = new IntersectionObserver((entries) => {
		entries.forEach((entry) => {
			if (entry.isIntersecting) {
				entry.target.classList.add("animate-on-scroll");
			}
		});
	}, observerOptions);

	// Observe elements for animation
	const elementsToAnimate = document.querySelectorAll(
		".about-section, .timeline-item, .interest-item, .goal-item, .philosophy-content"
	);
	elementsToAnimate.forEach((el) => observer.observe(el));
}

// Error handling for images
document.addEventListener("DOMContentLoaded", function () {
	const images = document.querySelectorAll("img");
	images.forEach((img) => {
		img.addEventListener("error", function () {
			this.style.display = "none";
			console.warn(`Failed to load image: ${this.src}`);
		});
	});
});

